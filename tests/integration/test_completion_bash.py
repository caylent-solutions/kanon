@pytest.mark.integration
def test_dynamic_row_add_with_at_routes_project_versions(tmp_path: Path) -> None:
    """AC-FUNC-004 (with-@ path): 'kanon add foo@<TAB>' resolves repo URL then lists versions.

    _kanon_complete_add_arg detects the '@' separator, shells out to
    kanon __resolve_entry_to_repo_url foo to get the repo URL, then calls
    _kanon_complete_project_versions with that URL and the empty spec.
    COMPREPLY must contain the project versions returned by the stub.
    """
    _assert_bash_version_gte_4()

    repo_url = "https://example.com/foo.git"
    versions_output = "1.0.0\n2.0.0\n"

    stub_dir = tmp_path / "stub_bin"
    stub_dir.mkdir(exist_ok=True)
    call_log = tmp_path / "kanon_calls.log"

    resolve_out = tmp_path / "out_resolve.txt"
    resolve_out.write_text(repo_url + "\n", encoding="utf-8")
    versions_out = tmp_path / "out_versions.txt"
    versions_out.write_text(versions_output, encoding="utf-8")

    stub_content = textwrap.dedent(f"""\
        #!/bin/bash
        # Stub kanon: supports __resolve_entry_to_repo_url and
        # __complete_project_versions.
        printf '%s\\n' "$*" >> {call_log}
        subcommand="${{1:-}}"
        case "$subcommand" in
            "__resolve_entry_to_repo_url") cat {resolve_out} ;;
            "__complete_project_versions") cat {versions_out} ;;
            "__complete_catalog_entries") ;;
            *) ;;
        esac
        """)
    stub_path = stub_dir / "kanon"
    stub_path.write_text(stub_content, encoding="utf-8")
    stub_path.chmod(stub_path.stat().st_mode | stat.S_IEXEC | stat.S_IXGRP | stat.S_IXOTH)

    candidates = _bash_preamble_runner(
        tmp_path=tmp_path,
        helper_call="_kanon_complete_add_arg 'foo@'",
        stub_bin_dir=str(stub_dir),
        extra_env={"KANON_COMPLETION_ENABLED": "1"},
    )

    assert "1.0.0" in candidates, f"Expected '1.0.0' in COMPREPLY for 'foo@' completion, got {candidates!r}"
    assert "2.0.0" in candidates, f"Expected '2.0.0' in COMPREPLY for 'foo@' completion, got {candidates!r}"

    call_log_text = Path(call_log).read_text(encoding="utf-8") if Path(call_log).exists() else ""
    assert "__resolve_entry_to_repo_url" in call_log_text, (
        f"Expected __resolve_entry_to_repo_url call for 'foo@', got: {call_log_text!r}"
    )
    assert "__complete_project_versions" in call_log_text, (
        f"Expected __complete_project_versions call for 'foo@', got: {call_log_text!r}"
    )



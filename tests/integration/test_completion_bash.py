@pytest.mark.integration
@pytest.mark.parametrize(
    "test_id,comp_line,comp_words,comp_cword,expected,exact_match",
    [pytest.param(*row, id=row[0]) for row in _STATIC_ROWS],
)
def test_static_completion_row(
    tmp_path: Path,
    test_id: str,
    comp_line: str,
    comp_words: list[str],
    comp_cword: int,
    expected: list[str],
    exact_match: bool,
) -> None:
    """AC-FUNC-002: static-enum rows return the expected candidate set via _shtab_kanon.

    For each row: set COMP_LINE/COMP_WORDS/COMP_CWORD, invoke _shtab_kanon,
    assert COMPREPLY matches or contains the expected candidates.
    """
    _assert_bash_version_gte_4()
    candidates = _bash_completion_runner(
        tmp_path=tmp_path,
        comp_line=comp_line,
        comp_words=comp_words,
        comp_cword=comp_cword,
    )
    if exact_match:
        assert sorted(candidates) == sorted(expected), (
            f"[{test_id}] Expected exact COMPREPLY={sorted(expected)!r}, got {sorted(candidates)!r}"
        )
    else:
        missing = [c for c in expected if c not in candidates]
        assert not missing, (
            f"[{test_id}] Expected COMPREPLY to contain {expected!r}, "
            f"but missing {missing!r}. Got: {sorted(candidates)!r}"
        )


@pytest.mark.integration
def test_bootstrap_offers_no_bootstrap_specific_flag_completion(tmp_path: Path) -> None:
    """`kanon bootstrap --<TAB>` offers no bootstrap-specific flag candidates.

    bootstrap was removed in a major release and is no longer a registered
    subcommand. Completion at `kanon bootstrap --` therefore falls through to
    the top-level parser and offers only the global flags; the old
    `--output-dir`/`--catalog-source` bootstrap completions are gone.
    """
    _assert_bash_version_gte_4()
    candidates = _bash_completion_runner(
        tmp_path=tmp_path,
        comp_line="kanon bootstrap --",
        comp_words=["kanon", "bootstrap", "--"],
        comp_cword=2,
    )
    flag_candidates = [c for c in candidates if c.startswith("-")]
    assert "--output-dir" not in flag_candidates, (
        f"bootstrap-specific '--output-dir' completion must be gone, got: {flag_candidates!r}"
    )
    assert "--catalog-source" not in flag_candidates, (
        f"bootstrap-specific '--catalog-source' completion must be gone, got: {flag_candidates!r}"
    )


def _assert_stub_called_with(call_log_path: str, expected_subcommand: str, expected_token: Optional[str]) -> None:
    """Assert the stub kanon was invoked with the expected subcommand and token."""
    call_log = Path(call_log_path)
    assert call_log.exists(), (
        f"Stub call log not found at {call_log_path!r}; did the completion script invoke kanon {expected_subcommand!r}?"
    )
    calls = call_log.read_text(encoding="utf-8").strip()
    assert expected_subcommand in calls, (
        f"Expected stub to be called with subcommand {expected_subcommand!r}, got call log: {calls!r}"
    )
    if expected_token is not None:
        assert expected_token in calls, f"Expected token {expected_token!r} in stub call log, got: {calls!r}"



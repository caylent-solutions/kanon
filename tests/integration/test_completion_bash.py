"""Integration tests for bash shell completion via compgen -F / _shtab_kanon.

Implements Section 11.9 acceptance item 1: every row in the Section 11.2
completion-coverage matrix has a passing integration test asserting the
expected candidate list.

Each parametrized case corresponds to one row of Section 11.2.  The test
runner sources the live ``kanon completion bash`` output into a real bash
process (>= 4.0).

For STATIC rows (choices-based flags and subcommand enums), the test calls
``_shtab_kanon`` with the appropriate ``COMP_LINE`` / ``COMP_WORDS`` /
``COMP_CWORD`` environment and captures ``COMPREPLY``.

For DYNAMIC rows, the test calls the preamble helper functions directly
(e.g., ``_kanon_complete_catalog_entries``, ``_kanon_complete_add_arg``).
The preamble helpers are defined in the completion script generated by
``kanon completion bash`` and are available after sourcing that script.
The test places a stub ``kanon`` executable on ``$PATH`` and asserts:
  (a) the stub was invoked exactly once with the expected subcommand and
      ``<current-token>`` argument, and
  (b) ``COMPREPLY`` matches the stub's output.

AC-FUNC-006 -- bash version check -- is an assertion (not a skip) so the
suite fails loudly on a bash < 4.0 interpreter.

AC-FUNC-007 -- the generated completion script is validated with ``bash -n``
before every parametrized case.
"""

from __future__ import annotations

import os
import re
import stat
import subprocess
import sys
import tempfile
import textwrap
from pathlib import Path
from typing import Optional

import pytest


def _kanon_completion_script() -> str:
    """Return the live ``kanon completion bash`` output."""
    result = subprocess.run(
        [sys.executable, "-m", "kanon_cli", "completion", "bash"],
        capture_output=True,
        text=True,
    )
    assert result.returncode == 0, (
        f"kanon completion bash failed with exit {result.returncode}. stderr: {result.stderr!r}"
    )
    assert result.stdout.strip(), "kanon completion bash produced empty output"
    return result.stdout


def _bash_syntax_check(script_content: str) -> None:
    """Assert that ``script_content`` is valid bash syntax via ``bash -n``."""
    with tempfile.NamedTemporaryFile(mode="w", suffix=".bash", delete=False) as tmp:
        tmp.write(script_content)
        tmp_path = tmp.name
    try:
        check = subprocess.run(["bash", "-n", tmp_path], capture_output=True, text=True)
        assert check.returncode == 0, f"bash -n syntax check failed.\nstderr: {check.stderr!r}"
    finally:
        os.unlink(tmp_path)


def _assert_bash_version_gte_4() -> None:
    """Assert the bash interpreter is >= 4.0; fail with an actionable message if not.

    AC-FUNC-006: This is NOT a skip -- a bash < 4.0 environment means the
    completion system will not function and is a test infrastructure failure.
    """
    result = subprocess.run(["bash", "--version"], capture_output=True, text=True)
    assert result.returncode == 0, (
        "Could not determine bash version: 'bash --version' failed. Ensure bash is installed and on PATH."
    )

    first_line = result.stdout.splitlines()[0] if result.stdout else ""
    match = re.search(r"version\s+(\d+)\.(\d+)", first_line)
    assert match is not None, (
        f"Could not parse bash version from output: {first_line!r}. Ensure bash >= 4.0 is installed."
    )
    major = int(match.group(1))
    minor = int(match.group(2))
    assert (major, minor) >= (4, 0), (
        f"bash version {major}.{minor} is too old. "
        "kanon shell completion requires bash >= 4.0. "
        "macOS stock bash 3.2 is NOT supported; install bash via Homebrew "
        "('brew install bash') and ensure it is first on PATH."
    )


def _write_stub_kanon(
    tmp_path: Path,
    subcommand_responses: Optional[dict[str, str]] = None,
) -> tuple[str, str]:
    """Write a stub ``kanon`` executable that records calls and returns fixture output.

    The stub handles ``kanon <subcommand> [args...]`` calls.  Each
    ``subcommand_responses`` key is a subcommand name; its value is the
    newline-delimited stdout the stub should emit for that subcommand.

    Args:
        tmp_path: Temporary directory used for the stub and call log.
        subcommand_responses: Mapping from subcommand name to fixture output.

    Returns:
        Tuple of (stub_bin_dir, call_log_path).  ``stub_bin_dir`` must be
        prepended to ``PATH`` before sourcing the completion script.
    """
    stub_dir = tmp_path / "stub_bin"
    stub_dir.mkdir(exist_ok=True)
    call_log = tmp_path / "kanon_calls.log"

    case_branches = ""
    if subcommand_responses:
        for subcmd, output in subcommand_responses.items():
            out_file = tmp_path / f"out_{subcmd.lstrip('_')}.txt"
            out_file.write_text(output, encoding="utf-8")
            case_branches += f'        "{subcmd}") cat {out_file} ;;\n'

    stub_content = textwrap.dedent(f"""\
        #!/bin/bash
        # Stub kanon: records invocations, returns configured fixture output.
        printf '%s\\n' "$*" >> {call_log}
        subcommand="${{1:-}}"
        case "$subcommand" in
        {case_branches}
            *) ;;
        esac
        """)
    stub_path = stub_dir / "kanon"
    stub_path.write_text(stub_content, encoding="utf-8")
    stub_path.chmod(stub_path.stat().st_mode | stat.S_IEXEC | stat.S_IXGRP | stat.S_IXOTH)
    return str(stub_dir), str(call_log)


def _bash_completion_runner(
    tmp_path: Path,
    comp_line: str,
    comp_words: list[str],
    comp_cword: int,
    stub_bin_dir: Optional[str] = None,
    extra_env: Optional[dict[str, str]] = None,
) -> list[str]:
    """Source the live completion script and return COMPREPLY from ``_shtab_kanon``.

    This fixture is reused by every parametrized static-row case (AC-TEST-002).
    It:
    1. Captures the live ``kanon completion bash`` output and writes it to a
       temp file.
    2. Verifies the script parses cleanly under ``bash -n`` (AC-FUNC-007).
    3. Spawns ``bash -c`` that sources the script, sets the COMP_* env,
       calls ``_shtab_kanon``, and prints each COMPREPLY item prefixed with
       ``CANDIDATE:``.
    4. Returns the list of printed items.

    Args:
        tmp_path: Temporary directory for the completion script and outputs.
        comp_line: Full command line as the user typed it (``COMP_LINE``).
        comp_words: Tokenized words (``COMP_WORDS``).
        comp_cword: Index of the word being completed (``COMP_CWORD``).
        stub_bin_dir: Optional directory prepended to ``PATH`` for stub kanon.
        extra_env: Optional additional environment variables for the subprocess.

    Returns:
        Sorted list of completion candidates from ``COMPREPLY``.
    """
    script_content = _kanon_completion_script()
    _bash_syntax_check(script_content)

    completion_file = tmp_path / "kanon_completion.bash"
    completion_file.write_text(script_content, encoding="utf-8")

    comp_words_bash = " ".join(f'"{w}"' for w in comp_words)
    path_prefix = f'"{stub_bin_dir}":' if stub_bin_dir else ""

    bash_script = textwrap.dedent(f"""\
        export PATH={path_prefix}"$PATH"
        source {completion_file}
        COMP_LINE={comp_line!r}
        COMP_WORDS=({comp_words_bash})
        COMP_CWORD={comp_cword}
        COMP_POINT=${{#COMP_LINE}}
        _shtab_kanon
        for item in "${{COMPREPLY[@]}}"; do
            printf 'CANDIDATE:%s\\n' "$item"
        done
        """)

    merged_env = {**os.environ, **(extra_env or {})}
    result = subprocess.run(
        ["bash", "-c", bash_script],
        capture_output=True,
        text=True,
        env=merged_env,
    )
    assert result.returncode == 0, (
        f"Bash completion script failed (exit {result.returncode}).\n"
        f"stdout: {result.stdout!r}\nstderr: {result.stderr!r}"
    )
    candidates = [line[len("CANDIDATE:") :] for line in result.stdout.splitlines() if line.startswith("CANDIDATE:")]
    return sorted(candidates)


def _bash_preamble_runner(
    tmp_path: Path,
    helper_call: str,
    stub_bin_dir: Optional[str] = None,
    extra_env: Optional[dict[str, str]] = None,
) -> list[str]:
    """Source the live completion script and call a preamble helper directly.

    Used for DYNAMIC rows where the completion is invoked through the preamble
    helper functions (e.g., ``_kanon_complete_catalog_entries``) rather than
    through ``_shtab_kanon``'s COMPGEN dispatch.

    This fixture is reused by every parametrized dynamic-row case (AC-TEST-002).
    It:
    1. Captures the live ``kanon completion bash`` output and writes it to a
       temp file.
    2. Verifies the script parses cleanly under ``bash -n`` (AC-FUNC-007).
    3. Spawns ``bash -c`` that sources the script, initialises COMPREPLY,
       calls the specified helper, and prints each COMPREPLY item prefixed
       with ``CANDIDATE:``.
    4. Returns the list of printed items.

    Args:
        tmp_path: Temporary directory for the completion script.
        helper_call: Bash expression to call the preamble helper, e.g.
            ``'_kanon_complete_catalog_entries ""'``.
        stub_bin_dir: Optional directory prepended to ``PATH`` for stub kanon.
        extra_env: Optional additional environment variables.

    Returns:
        Sorted list of completion candidates from ``COMPREPLY``.
    """
    script_content = _kanon_completion_script()
    _bash_syntax_check(script_content)

    completion_file = tmp_path / "kanon_completion.bash"
    completion_file.write_text(script_content, encoding="utf-8")

    path_prefix = f'"{stub_bin_dir}":' if stub_bin_dir else ""

    bash_script = textwrap.dedent(f"""\
        export PATH={path_prefix}"$PATH"
        source {completion_file}
        COMPREPLY=()
        {helper_call}
        for item in "${{COMPREPLY[@]}}"; do
            printf 'CANDIDATE:%s\\n' "$item"
        done
        """)

    merged_env = {**os.environ, **(extra_env or {})}
    result = subprocess.run(
        ["bash", "-c", bash_script],
        capture_output=True,
        text=True,
        env=merged_env,
    )
    assert result.returncode == 0, (
        f"Bash preamble helper script failed (exit {result.returncode}).\n"
        f"stdout: {result.stdout!r}\nstderr: {result.stderr!r}"
    )
    candidates = [line[len("CANDIDATE:") :] for line in result.stdout.splitlines() if line.startswith("CANDIDATE:")]
    return sorted(candidates)



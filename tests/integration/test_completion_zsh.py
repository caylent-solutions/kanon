def _assert_zsh_available() -> None:
    """Assert zsh is available on PATH; fail with actionable message if not.

    AC-FUNC-008: This is NOT a skip -- a missing zsh environment means the
    completion system will not function and is a test infrastructure failure.
    """
    result = subprocess.run(["which", "zsh"], capture_output=True, text=True)
    assert result.returncode == 0, (
        "zsh is not available on PATH -- cannot run zsh completion integration tests. "
        "Install zsh via your package manager: 'apt-get install zsh' (Debian/Ubuntu), "
        "'yum install zsh' (RHEL/CentOS), or 'brew install zsh' (macOS)."
    )


def _kanon_completion_script() -> str:
    """Return the live ``kanon completion zsh`` output."""
    result = subprocess.run(
        [sys.executable, "-m", "kanon_cli", "completion", "zsh"],
        capture_output=True,
        text=True,
    )
    assert result.returncode == 0, (
        f"kanon completion zsh failed with exit {result.returncode}. stderr: {result.stderr!r}"
    )
    assert result.stdout.strip(), "kanon completion zsh produced empty output"
    return result.stdout


def _zsh_syntax_check(script_content: str) -> None:
    """Assert that ``script_content`` is valid zsh syntax via ``zsh -n``."""
    with tempfile.NamedTemporaryFile(mode="w", suffix=".zsh", delete=False) as tmp:
        tmp.write(script_content)
        tmp_path = tmp.name
    try:
        check = subprocess.run(["zsh", "-n", tmp_path], capture_output=True, text=True)
        assert check.returncode == 0, f"zsh -n syntax check failed.\nstderr: {check.stderr!r}"
    finally:
        os.unlink(tmp_path)


def _write_completion_file(tmp_path: Path) -> str:
    """Write the live completion script to a temp file and return the path."""
    script_content = _kanon_completion_script()
    _zsh_syntax_check(script_content)
    completion_file = tmp_path / "kanon_completion.zsh"
    completion_file.write_text(script_content, encoding="utf-8")
    return str(completion_file)


def _write_stub_kanon(
    tmp_path: Path,
    subcommand_responses: Optional[dict[str, str]] = None,
) -> tuple[str, str]:
    """Write a stub ``kanon`` executable that records calls and returns fixture output.

    The stub handles ``kanon <subcommand> [args...]`` calls.  Each
    ``subcommand_responses`` key is a subcommand name; its value is the
    newline-delimited stdout the stub should emit for that subcommand.

    Args:
        tmp_path: Temporary directory used for the stub and call log.
        subcommand_responses: Mapping from subcommand name to fixture output.

    Returns:
        Tuple of (stub_bin_dir, call_log_path).  ``stub_bin_dir`` must be
        prepended to ``PATH`` before sourcing the completion script.
    """
    stub_dir = tmp_path / "stub_bin"
    stub_dir.mkdir(exist_ok=True)
    call_log = tmp_path / "kanon_calls.log"

    case_branches = ""
    if subcommand_responses:
        for subcmd, output in subcommand_responses.items():
            out_file = tmp_path / f"out_{subcmd.lstrip('_')}.txt"
            out_file.write_text(output, encoding="utf-8")
            case_branches += f'        "{subcmd}") cat {out_file} ;;\n'

    stub_content = textwrap.dedent(f"""\
        #!/bin/bash
        # Stub kanon: records invocations, returns configured fixture output.
        printf '%s\\n' "$*" >> {call_log}
        subcommand="${{1:-}}"
        case "$subcommand" in
        {case_branches}
            *) ;;
        esac
        """)
    stub_path = stub_dir / "kanon"
    stub_path.write_text(stub_content, encoding="utf-8")
    stub_path.chmod(stub_path.stat().st_mode | stat.S_IEXEC | stat.S_IXGRP | stat.S_IXOTH)
    return str(stub_dir), str(call_log)


def _zsh_completion_runner(
    tmp_path: Path,
    helper_call: str,
    stub_bin_dir: Optional[str] = None,
    extra_env: Optional[dict[str, str]] = None,
) -> list[str]:
    """Source the live completion script and capture ``compadd`` candidates.

    This is the central fixture reused by every parametrized dynamic-row case
    (AC-TEST-002).  It:
    1. Captures the live ``kanon completion zsh`` output and writes it to a
       temp file.
    2. Verifies the script parses cleanly under ``zsh -n`` (AC-FUNC-007).
    3. Spawns ``zsh -fc`` that overrides ``compadd`` and ``_arguments`` to
       capture candidates, sources the completion script, and invokes the
       specified helper expression.
    4. Returns the list of captured candidate items.

    ``compadd`` is overridden to collect all non-flag arguments.
    ``_arguments`` is overridden with a no-op so that sourcing the script
    succeeds outside a real completion context (zsh's ``_arguments`` can only
    run inside a completion function).

    Args:
        tmp_path: Temporary directory for the completion script and outputs.
        helper_call: Zsh expression to invoke, e.g.
            ``'_kanon_complete_catalog_entries ""'``.
        stub_bin_dir: Optional directory prepended to ``PATH`` for stub kanon.
        extra_env: Optional additional environment variables for the subprocess.

    Returns:
        Sorted list of completion candidates captured from ``compadd``.
    """
    completion_file = _write_completion_file(tmp_path)
    path_prefix = f'"{stub_bin_dir}":' if stub_bin_dir else ""

    zsh_script = textwrap.dedent(f"""\
        export PATH={path_prefix}"$PATH"
        # Override _arguments: no-op so sourcing works outside completion context.
        _arguments() {{ return 0; }}
        # Override compdef: no-op registration.
        compdef() {{ return 0; }}
        # Override compadd: capture non-flag arguments as candidates.
        _captured_completions=()
        compadd() {{
            local saw_dashdash=0
            for arg in "$@"; do
                if [[ "$arg" == "--" ]]; then
                    saw_dashdash=1
                    continue
                fi
                if [[ $saw_dashdash -eq 1 ]]; then
                    [[ -n "$arg" ]] && _captured_completions+=("$arg")
                fi
            done
        }}
        source {completion_file}
        {helper_call}
        for item in "${{_captured_completions[@]}}"; do
            printf 'CANDIDATE:%s\\n' "$item"
        done
        """)

    merged_env = {**os.environ, **(extra_env or {})}
    result = subprocess.run(
        ["zsh", "-fc", zsh_script],
        capture_output=True,
        text=True,
        env=merged_env,
    )
    assert result.returncode == 0, (
        f"Zsh completion script failed (exit {result.returncode}).\n"
        f"stdout: {result.stdout!r}\nstderr: {result.stderr!r}"
    )
    candidates = [line[len("CANDIDATE:") :] for line in result.stdout.splitlines() if line.startswith("CANDIDATE:")]
    return sorted(candidates)


def _zsh_option_spec_runner(
    tmp_path: Path,
    option_array_name: str,
    label: str,
) -> list[str]:
    """Extract enum choices from a zsh option spec array by label.

    Sources the completion script with mocked ``_arguments`` and ``compdef``,
    then inspects the named option spec array for a spec element whose label
    matches ``label``.  Parses the parenthesised choice list and returns it.

    For example, ``_shtab_kanon_search_options`` contains a spec like::

        "--format[...]:list_format:(names json)"

    Calling this with ``label="list_format"`` returns ``["json", "names"]``.

    Args:
        tmp_path: Temporary directory for the completion script.
        option_array_name: Name of the zsh array to inspect, e.g.
            ``"_shtab_kanon_search_options"``.
        label: The positional-argument label to match, e.g. ``"list_format"``.

    Returns:
        Sorted list of choices extracted from the spec.
    """
    completion_file = _write_completion_file(tmp_path)

    zsh_script = textwrap.dedent(f"""\
        _arguments() {{ return 0; }}
        compdef() {{ return 0; }}
        _describe() {{ return 0; }}
        source {completion_file}
        local varname="{option_array_name}"
        local -a arr
        arr=("${{(@P)varname}}")
        for opt in "${{arr[@]}}"; do
            if [[ "$opt" == *":{label}:"* ]]; then
                local rest="${{opt##*:{label}:}}"
                # Strip surrounding parentheses
                rest="${{rest:1:-1}}"
                for choice in ${{=rest}}; do
                    printf 'CHOICE:%s\\n' "$choice"
                done
                break
            fi
        done
        """)

    result = subprocess.run(
        ["zsh", "-fc", zsh_script],
        capture_output=True,
        text=True,
    )
    assert result.returncode == 0, (
        f"Zsh option spec extraction failed (exit {result.returncode}).\n"
        f"stdout: {result.stdout!r}\nstderr: {result.stderr!r}"
    )
    choices = [line[len("CHOICE:") :] for line in result.stdout.splitlines() if line.startswith("CHOICE:")]
    return sorted(choices)


def _zsh_positional_spec_runner(
    tmp_path: Path,
    option_array_name: str,
    pattern: str,
) -> list[str]:
    """Extract enum choices from a positional spec element matching a pattern.

    Used for specs like ``:Target shell. Choices\\: \\{bash, zsh\\}.:(bash zsh)``
    where the label field is blank (positional).  Matches any spec element
    that contains ``pattern`` and ends with a parenthesised choice list.

    Args:
        tmp_path: Temporary directory for the completion script.
        option_array_name: Name of the zsh array to inspect.
        pattern: Substring to match within the spec element, e.g.
            ``"bash zsh"`` or ``"bash, zsh"``.

    Returns:
        Sorted list of choices parsed from the matching spec element.
    """
    completion_file = _write_completion_file(tmp_path)

    zsh_script = textwrap.dedent(f"""\
        _arguments() {{ return 0; }}
        compdef() {{ return 0; }}
        _describe() {{ return 0; }}
        source {completion_file}
        local varname="{option_array_name}"
        local -a arr
        arr=("${{(@P)varname}}")
        for opt in "${{arr[@]}}"; do
            if [[ "$opt" == *"{pattern}"* ]]; then
                # Find the last colon-paren group "(choices)" in the spec
                local rest="${{opt##*:}}"
                if [[ "$rest" == "("* ]]; then
                    rest="${{rest:1:-1}}"
                    for choice in ${{=rest}}; do
                        printf 'CHOICE:%s\\n' "$choice"
                    done
                    break
                fi
            fi
        done
        """)

    result = subprocess.run(
        ["zsh", "-fc", zsh_script],
        capture_output=True,
        text=True,
    )
    assert result.returncode == 0, (
        f"Zsh positional spec extraction failed (exit {result.returncode}).\n"
        f"stdout: {result.stdout!r}\nstderr: {result.stderr!r}"
    )
    choices = [line[len("CHOICE:") :] for line in result.stdout.splitlines() if line.startswith("CHOICE:")]
    return sorted(choices)


def _zsh_subcommand_runner(
    tmp_path: Path,
    commands_fn: str,
) -> list[str]:
    """Capture subcommand names emitted by a ``_shtab_*_commands`` function.

    Sources the completion script with mocked ``_arguments`` and ``compdef``,
    overrides ``_describe`` to extract the subcommand names from the local
    ``_commands`` array, then calls the specified function.

    Args:
        tmp_path: Temporary directory for the completion script.
        commands_fn: Name of the commands function to call, e.g.
            ``"_shtab_kanon_commands"``.

    Returns:
        Sorted list of subcommand names.
    """
    completion_file = _write_completion_file(tmp_path)

    zsh_script = textwrap.dedent(f"""\
        _arguments() {{ return 0; }}
        compdef() {{ return 0; }}
        _describe() {{
            local tag="$1"
            local inner_arr_name="$2"
            local -a inner_arr
            inner_arr=("${{(@P)inner_arr_name}}")
            for item in "${{inner_arr[@]}}"; do
                printf 'SUBCOMMAND:%s\\n' "${{item%%:*}}"
            done
        }}
        source {completion_file}
        {commands_fn}
        """)

    result = subprocess.run(
        ["zsh", "-fc", zsh_script],
        capture_output=True,
        text=True,
    )
    assert result.returncode == 0, (
        f"Zsh subcommand runner failed (exit {result.returncode}).\n"
        f"stdout: {result.stdout!r}\nstderr: {result.stderr!r}"
    )
    subcommands = [line[len("SUBCOMMAND:") :] for line in result.stdout.splitlines() if line.startswith("SUBCOMMAND:")]
    return sorted(subcommands)


def _assert_stub_called_with(
    call_log_path: str,
    expected_subcommand: str,
    expected_token: Optional[str] = None,
) -> None:
    """Assert the stub kanon was invoked with the expected subcommand and token."""
    call_log = Path(call_log_path)
    assert call_log.exists(), (
        f"Stub call log not found at {call_log_path!r}; did the completion script invoke kanon {expected_subcommand!r}?"
    )
    calls = call_log.read_text(encoding="utf-8").strip()
    assert expected_subcommand in calls, (
        f"Expected stub to be called with subcommand {expected_subcommand!r}, got call log: {calls!r}"
    )
    if expected_token is not None:
        assert expected_token in calls, f"Expected token {expected_token!r} in stub call log, got: {calls!r}"



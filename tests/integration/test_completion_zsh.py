"""Integration tests for zsh shell completion via _shtab_kanon (_main_complete).

Implements Section 11.9 acceptance item 1 and 6: every row in the Section 11.2
completion-coverage matrix has a passing integration test asserting the
expected candidate list.

Each parametrized case corresponds to one row of Section 11.2.  The test
runner sources the live ``kanon completion zsh`` output into a real zsh
process (>= 5.0).

For STATIC rows (choices-based flags and subcommand enums), the test inspects
the option spec arrays declared by the generated zsh completion script and
the subcommand-listing helper functions (e.g., ``_shtab_kanon_commands``).
These arrays ARE the source of truth for what ``_arguments`` will offer.

For DYNAMIC rows, the test calls the preamble helper functions directly
(e.g., ``_kanon_complete_catalog_entries``, ``_kanon_complete_add_arg``).
The preamble helpers are defined in the completion script generated by
``kanon completion zsh`` and are available after sourcing that script.
The test places a stub ``kanon`` executable on ``$PATH`` and asserts:
  (a) the stub was invoked exactly once with the expected subcommand and
      ``<current-token>`` argument, and
  (b) ``compadd`` was called with the stub's output.

AC-FUNC-008 -- zsh availability check -- is an assertion (not a skip) so the
suite fails loudly when zsh is not installed.

AC-FUNC-007 -- the generated completion script is validated with ``zsh -n``
before every parametrized case.

AC-FUNC-004 -- the generated script starts with ``#compdef kanon``.
"""

